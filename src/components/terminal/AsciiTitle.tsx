export function AsciiTitle({ text, className }: AsciiTitleProps) {
  const art = asciiArt[text.toUpperCase()]
  
  if (!art) {
    // Fallback to simple text if no ASCII art exists
    return (
      <pre className={cn("text-accent text-sm leading-tight mb-4", className)}>
        {"═".repeat(text.length + 4)}{"\n"}
        {"║ "}{text}{" ║"}{"\n"}
        {"═".repeat(text.length + 4)}
      </pre>
    )
  }

  return (
    <pre className={cn("text-accent text-[10px] leading-tight mb-6 overflow-x-auto", className)}>
      {art}
    </pre>
  )
}

// Smaller ASCII titles for sub-sections
export function AsciiDivider({ className }: { className?: string }) {
  return (
    <div className={cn("text-fg-muted text-xs my-4", className)}>
      ────────────────────────────────────────
    </div>
  )
}
